void Display(int iNo)
{
    if (iNo <= 10)
    {
        printf("Hello");
    }
    else
    {
        printf("Demo");
    }
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
} // End main

