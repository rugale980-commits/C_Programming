#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Accept number from user and print <10 print Hello,>10 Demo
//  Input         : Integer
//  Output        : Hello , Demo
//  Author        : Rahul Balasaheb Ugale
//  Date          : 21/10/2025
