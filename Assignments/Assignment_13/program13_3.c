#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_odd_number
//  Description   : Accept number from user & print all odd number up to N
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////

void print_odd_number(int limit)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= limit; iCnt++)
    {
        if (iCnt % 2 != 0)
        {
            printf(" %d ", iCnt);
            iCnt++;
        }
    }

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    print_odd_number(iValue);

    return 0;
} // End main

