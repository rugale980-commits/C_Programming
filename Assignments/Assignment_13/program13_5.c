#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : sum_even_natural
//  Description   : Accept number from user & print sum of first N even number
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////

int sum_even_natural(int limit)
{
    int iCnt = 0;
    int iSum = 0;

    for (iCnt = 1; iCnt <= limit; iCnt++)
    {
        iSum += 2 * iCnt;
    }
    return iSum;

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    iRet = sum_even_natural(iValue);

    printf("Sum of natural number %d", iRet);

    return 0;
} // End main

