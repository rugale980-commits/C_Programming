#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : sum_natural_number
//  Description   : Accept number from user & print sum of first N natural num.
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////

int sum_natural_number(int limit)
{
    int iCnt = 0;
    int iSum = 0;

    for (iCnt = 1; iCnt <= limit; iCnt++)
    {
        iSum += iCnt;
    }
    return iSum;

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    iRet = sum_natural_number(iValue);

    printf("Sum of natural number %d", iRet);

    return 0;
} // End main

