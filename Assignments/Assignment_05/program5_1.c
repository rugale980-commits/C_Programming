void CheckEvenOdd(int num)
{
    int iCnt = 0;

    if (num % 2 == 0)
    {
        printf("It is Even Number : %d", num);
    }
    else
    {
        printf("It is Odd Number : %d", num);
    }

} // End function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int number = 0;

    printf("Enter number : ");
    scanf("%d", &number);

    CheckEvenOdd(number);

    return 0;
} // End main

