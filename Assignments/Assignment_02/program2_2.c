#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Accept number from user and print * on screen reverse
//  Input         : Integer
//  Output        : *
//  Author        : Rahul Balasaheb Ugale
//  Date          : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////

void Display(int iNo)
{
    int iCnt = 0;

    // Updater
    if (iNo < 0)
    {
        iNo = -iNo;
    }
    while (iNo > iCnt)
    {
        printf("*");
        iNo--;
    }
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    Display(iValue);

    return 0;
} // End main

