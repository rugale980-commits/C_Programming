#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : SquarMeter
//  Description   : Accept area in squer feet & convert into squer meter
//  Input         : Integer
//  Output        : Double
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////

double SquarMeter(int ivalue)
{
    double iSquer = 0.0929;
    iSquer = ivalue * iSquer;
    return iSquer;

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    double dRet = 0.0;

    printf("Enter area in squer feet : ");
    scanf("%d", &iValue);

    dRet = SquarMeter(iValue);

    printf("Squer meter  is  %lf", dRet);

    return 0;
} // End main

