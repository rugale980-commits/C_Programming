#include <stdio.h>
#include <math.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CircleArea
//  Description   : Accept number from user & calculate radius of circle
//  Input         : Flaot
//  Output        : Double
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////
#define PI 3.14
double CircleArea(float fRadius)
{
    // Updater
    if (fRadius <= 0)
    {
        fRadius = -fRadius;
    }
    double Area = PI * fRadius * fRadius;
    return Area;

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0;
    double dRet = 0;

    printf("Enter radius : ");
    scanf("%f", &fValue);

    dRet = CircleArea(fValue);

    printf("Area  is  %lf", dRet);

    return 0;
} // End main

