#include <stdio.h>
#include <math.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : FhtoCs
//  Description   : Accept temperture from user & calculate faherenheit & celsiu
//  Input         : Flaot
//  Output        : Double
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////

double FhtoCs(float fTemp)
{
    double celsius = 0.0;
    celsius = (fTemp - 32.0f) * (5.0 / 9.0);
    return celsius;

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter temperature in fahrenheit : ");
    scanf("%f", &fValue);

    dRet = FhtoCs(fValue);

    printf("Celsius  is  %lf", dRet);

    return 0;
} // End main

