#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Accept
//  Description   : Accept number from user and print * on screen
//  Input         : Integer
//  Output        : *
//  Author        : Rahul Balasaheb Ugale
//  Date          : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*\n");
    }
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{

    int iValue = 0;
    // iValue = 5;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    Accept(iValue);
    return 0;
} // End main

