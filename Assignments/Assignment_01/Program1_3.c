#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Display
//  Description   : Program to print 5 to 1 number
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 20/10/2025
//
