void Display()
{
    int i = 0;
    i = 5;
    while (i >= 1)
    {
        printf("%d\n", i);
        i--;
    }
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
} // End main

