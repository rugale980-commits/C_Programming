#include <stdio.h>
#include <stdbool.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : Check
//  Description   : Program to print 5 number divisible or not
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 20/10/2025
//
////////////////////////////////////////////////////////////////////////////////

int check(int iNo)
{

    if ((iNo % 5) == 0)
    {
        return true;
    }
    else
    {
        return false;
    }
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    bool bRet = true;

    printf("Enter number : ");
    scanf("%d", &iValue);

    bRet = check(iValue);

    if (bRet == true)
    {
        printf("Division by 5");
    }
    else
    {
        printf("Not Division by 5");
    }

    return 0;
} // End main

