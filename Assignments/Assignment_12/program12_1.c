#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : print_factors
//  Description   : Accept number from user & print all factors
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 22/10/2025
//
////////////////////////////////////////////////////////////////////////////////

void print_factors(int number)
{
    int iCnt = 0;

    for (iCnt = 1; iCnt <= number; iCnt++)
    {
        if (number % iCnt == 0)
        {
            printf(" %d ", iCnt);
        }
    }

} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the number : ");
    scanf("%d", &iValue);

    print_factors(iValue);

    return 0;
} // End main

