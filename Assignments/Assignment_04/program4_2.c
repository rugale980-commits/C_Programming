////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : MultRev
//  Description   : Accept number from user & display factor decreasing order
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 21/10/2025
