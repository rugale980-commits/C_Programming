void DispayConvert(char CValue)
{

    if (CValue >= 'A' && CValue <= 'Z')
    {
        CValue = CValue + 32;
        printf("Converted character: %c", CValue);
    }
    else if (CValue >= 'a' && CValue <= 'z')
    {
        CValue = CValue - 32;
        printf("Converted character: %c", CValue);
    }
    printf("\n");
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = 0;

    printf("Enter Character : ");
    scanf("%c", &cValue);

    DispayConvert(cValue);

    return 0;
} // End main

