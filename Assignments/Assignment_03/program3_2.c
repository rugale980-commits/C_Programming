////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DisplayFactor
//  Description   : Accept number from user & print even factors number
//  Input         : Integer
//  Output        : Integer
//  Author        : Rahul Balasaheb Ugale
//  Date          : 21/10/2025
//
////////////////////////////////////////////////////////////////////////////////

void DispayFactor(int iNo)
{
    int iCnt = 0;
    // Updater
    if (iNo <= 0)
    {
        iNo = -iNo;
    }

    for (iCnt = 1; iCnt <= iNo / 2; iCnt++)
    {
        if (iNo % iCnt == 0)
        {
            printf("%d ", iCnt);
        }
    }
    printf("\n");
} // End Function

////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
//
////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter the Number : ");
    scanf("%d", &iValue);

    DispayFactor(iValue);

    return 0;
} // End main

